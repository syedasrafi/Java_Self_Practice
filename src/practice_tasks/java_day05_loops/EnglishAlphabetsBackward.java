package practice_tasks.java_day05_loops;

public class EnglishAlphabetsBackward {
    public static void main(String[] args) {

        //char Alphabet = 'A';

        for (char i = 'Z'; i >= 'A'; i--) {
            System.out.print(i + " ");
        }

    }
}

